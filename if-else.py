# if-else statement in Python

# Example-1
x = 10
y = 20

if x > y:
    print("x is greater than y")
else:
    print("y is greater than x")

# elif statement

# Example-2
x = 10
y = 20

if x > y:
    print("x is greater than y")
elif x < y:
    print("x is less than y")
else:
    print("x is equal to y")